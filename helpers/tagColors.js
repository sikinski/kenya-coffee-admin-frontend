// Функция для определения, использовать ли белый текст на фоне
// Возвращает true, если нужен белый текст (для темных/ярких цветов)
export const shouldUseWhiteText = (backgroundColor) => {
    if (!backgroundColor) return false
    
    // Удаляем # если есть
    const hex = backgroundColor.replace('#', '')
    
    // Конвертируем hex в RGB
    const r = parseInt(hex.substr(0, 2), 16)
    const g = parseInt(hex.substr(2, 2), 16)
    const b = parseInt(hex.substr(4, 2), 16)
    
    // Вычисляем яркость по формуле W3C
    const brightness = (r * 299 + g * 587 + b * 114) / 1000
    
    // Если яркость меньше 128, используем белый текст
    return brightness < 128
}

